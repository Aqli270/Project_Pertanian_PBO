    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       try {
           int id = Integer.parseInt(txtId.getText());
           
           int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data dengan ID: "+ id + "?", "Konfirmasi Penghapusan", JOptionPane.YES_NO_OPTION);
           
           if (confirm == JOptionPane.YES_OPTION) {
            // Panggil metode delete dari AlatBahanDAO
            alatBahanDAO.delete(id);

            // Tampilkan pesan sukses
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
       
        } catch (NumberFormatException ex) {
        // Tampilkan pesan error jika ID tidak valid
        JOptionPane.showMessageDialog(this, "Pastikan ID diisi dengan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
        // Tampilkan pesan error jika terjadi kesalahan SQL
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

