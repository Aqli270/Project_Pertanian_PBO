    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            int id = Integer.parseInt(txtId.getText());
            String kategori = txtKategori.getText();
            String nama = txtNama.getText();
            String jenis = txtJenis.getText();
            String satuan = txtSatuan.getText();
            int stok = Integer.parseInt(txtStok.getText());
            int harga = Integer.parseInt(txtHarga.getText());
            
            AlatBahan alatBahan = new AlatBahan(id, kategori, nama, jenis, satuan, stok, harga);    
            alatBahanDAO.create(alatBahan);
            
      JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Pastikan ID, Stok, dan Harga diisi dengan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }     
    }//GEN-LAST:event_btnSimpanActionPerformed

